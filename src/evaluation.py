# FIGURE 6 — ROC Curves
# ══════════════════════════════════════════════════════════════════════════════
def plot_roc_curves(roc_data: Dict,
                    save_path: str = 'figures/fig6_roc_curves.pdf'):
    """
    Figure 6: ROC curves for top 6 models.
    Clean IEEE format with AUC annotations.
    """
    fig, ax = plt.subplots(figsize=(3.5, 3.3))

    # Model display order and styles
    plot_order = [
        ('Logistic Regression + TF-IDF', COLORS['baseline_ml'],  '--',  1.0),
        ('CNN (Character-level)',         COLORS['baseline_dl'],  '-.',  1.2),
        ('RNN-GRU',                       '#9CA3AF',              ':',   1.2),
        ('DistilBERT-only (FP32)',         '#93C5FD',              '--',  1.4),
        ('Teacher (BERT+TF-IDF, FP32)',   COLORS['teacher'],      '-',   1.8),
        ('EdgePhish-5G (INT8-QAT)',        COLORS['edgephish_int8'], '-', 2.2),
    ]

    for model_name, color, ls, lw in plot_order:
        if model_name not in roc_data:
            continue
        fpr = np.array(roc_data[model_name]['fpr'])
        tpr = np.array(roc_data[model_name]['tpr'])
        auc = roc_data[model_name]['auc']

        short = (model_name
                 .replace(' + TF-IDF','').replace(' (Character-level)','')
                 .replace('Teacher (BERT+TF-IDF, FP32)', 'Teacher')
                 .replace('EdgePhish-5G (INT8-QAT)', 'EdgePhish-5G (Proposed)')
                 .replace('DistilBERT-only (FP32)', 'DistilBERT'))

        ax.plot(fpr, tpr, color=color, linestyle=ls, linewidth=lw,
                label=f'{short} (AUC={auc:.4f})', zorder=4 if 'EdgePhish' in model_name else 3)

    # Diagonal reference
    ax.plot([0,1],[0,1], 'k--', linewidth=0.8, alpha=0.5, label='Random (AUC=0.5000)')

    # Zoom inset on top-left
    ax.set_xlim(-0.01, 1.01)
    ax.set_ylim(-0.01, 1.01)
    ax.set_xlabel('False Positive Rate', fontsize=9)
    ax.set_ylabel('True Positive Rate', fontsize=9)
    ax.set_title('Figure 6: ROC Curves', fontsize=9.5, pad=5)
    ax.legend(fontsize=6.5, loc='lower right', framealpha=0.92)

    plt.tight_layout()
    plt.savefig(save_path, dpi=300)
    plt.savefig(save_path.replace('.pdf', '.png'), dpi=300)
    plt.close()
    print(f'  ✓ Figure 6 saved: {save_path}')


