# FIGURE 3 — F1-Score Comparison Bar Chart
# ══════════════════════════════════════════════════════════════════════════════
def plot_f1_comparison(results: Dict, save_path: str = 'figures/fig3_f1_comparison.pdf'):
    """
    Figure 3: Grouped bar chart comparing F1-scores across all models.

    Layout: Horizontal bars grouped by model family, sorted by F1 within group.
    Annotation: Exact F1 values on each bar. Teacher marked with dashed line.
    SLA annotation: EdgePhish-5G INT8 highlighted as 'proposed'.
    """
    # Prepare data — select main models for paper (not all ablations)
    display_models = [
        ('Logistic Regression + TF-IDF',    'Baseline-ML'),
        ('SVM + TF-IDF',                    'Baseline-ML'),
        ('Random Forest + TF-IDF',          'Baseline-ML'),
        ('Gradient Boosting + TF-IDF',      'Baseline-ML'),
        ('CNN (Character-level)',            'Baseline-DL'),
        ('RNN-GRU',                         'Baseline-DL'),
        ('DistilBERT-only (INT8)',           'Baseline-DL'),
        ('DistilBERT-only (FP32)',           'Baseline-DL'),
        ('EdgePhish-5G (TF-IDF-only, Ablation)', 'EdgePhish-Ablation'),
        ('EdgePhish-5G (BERT-only, Ablation)',    'EdgePhish-Ablation'),
        ('EdgePhish-5G (Concat Fusion)',          'EdgePhish-Ablation'),
        ('Teacher (BERT+TF-IDF, FP32)',      'Teacher'),
        ('EdgePhish-5G (FP32)',              'EdgePhish-Proposed'),
        ('EdgePhish-5G (INT8-QAT)',          'EdgePhish-Proposed'),
    ]

    labels, f1_vals, colors, hatches = [], [], [], []
    for model_name, group in display_models:
        if model_name in results:
            short = (model_name
                     .replace(' + TF-IDF','').replace(' (Character-level)','')
                     .replace(' (Ablation)','').replace('Gradient Boosting','GBoost')
                     .replace('EdgePhish-5G','EdgePhish')
                     .replace('DistilBERT-only','DistilBERT')
                     .replace('Teacher (BERT+TF-IDF, FP32)','Teacher'))
            labels.append(short)
            f1_vals.append(results[model_name]['f1'])
            colors.append(GROUP_COLORS.get(group, '#6B7280'))
            hatches.append('///' if group == 'EdgePhish-Proposed' else '')

    fig, ax = plt.subplots(figsize=(7.0, 5.2))
    y_pos = np.arange(len(labels))
    bars = ax.barh(y_pos, f1_vals, height=0.65,
                   color=colors, edgecolor='white', linewidth=0.5)

    # Hatch proposed model bars
    for bar, hatch in zip(bars, hatches):
        if hatch:
            bar.set_hatch(hatch)
            bar.set_edgecolor('#065F46')

    # Value annotations
    for i, (bar, val) in enumerate(zip(bars, f1_vals)):
        ax.text(val + 0.05, bar.get_y() + bar.get_height()/2,
                f'{val:.2f}%', va='center', ha='left', fontsize=7.5,
                fontweight='bold' if colors[i] == COLORS['edgephish_int8'] else 'normal')

    # Teacher reference line
    teacher_f1 = results['Teacher (BERT+TF-IDF, FP32)']['f1']
    ax.axvline(teacher_f1, color=COLORS['teacher'], linestyle='--',
               linewidth=1.4, alpha=0.8, label=f'Teacher F1 ({teacher_f1:.2f}%)')

    # 5G latency budget annotation
    ax.set_xlim(82, 101.5)
    ax.set_ylim(-0.5, len(labels) - 0.5)
    ax.set_yticks(y_pos)
    ax.set_yticklabels(labels, fontsize=8)
    ax.set_xlabel('F1-Score (%)', fontsize=9)
    ax.set_title('Figure 3: F1-Score Comparison — EdgePhish-5G vs. Baselines', fontsize=10, pad=8)

    # Legend for model groups
    legend_elements = [
        mpatches.Patch(color=COLORS['baseline_ml'],       label='Traditional ML'),
        mpatches.Patch(color=COLORS['baseline_dl'],       label='Deep Learning'),
        mpatches.Patch(color=COLORS['teacher'],           label='Teacher Model'),
        mpatches.Patch(color=COLORS['edgephish_ablation'],label='EdgePhish (Ablation)'),
        mpatches.Patch(color=COLORS['edgephish_int8'],    label='EdgePhish (Proposed)', hatch='///'),
        Line2D([0],[0], color=COLORS['teacher'], linestyle='--', label='Teacher F1 Reference'),
    ]
    ax.legend(handles=legend_elements, loc='lower right', fontsize=7.5,
              framealpha=0.9, edgecolor='#E5E7EB')

    ax.xaxis.set_minor_locator(MultipleLocator(0.5))
    plt.tight_layout()
    plt.savefig(save_path, dpi=300)
    plt.savefig(save_path.replace('.pdf', '.png'), dpi=300)
    plt.close()
    print(f'  ✓ Figure 3 saved: {save_path}')


