    ax1.plot(loads[stable],  pred[stable],  's--', color=COLORS['teacher'],
             lw=1.8, ms=6, label='M/D/1 Predicted P99')
    ax1.plot(loads[~stable], meas[~stable], 'x', color=COLORS['highlight'],
             ms=10, mew=2, label='Unstable (ρ≥1)')

    ax1.axhline(10.0, color=COLORS['highlight'], linestyle='--', lw=1.4,
                alpha=0.7, label='5G SLA Budget (10ms)')
