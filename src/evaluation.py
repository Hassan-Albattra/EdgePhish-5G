# FIGURE 4 — Latency vs Throughput
# ══════════════════════════════════════════════════════════════════════════════
def plot_latency_throughput(latency: Dict,
                            save_path: str = 'figures/fig4_latency_throughput.pdf'):
    """
    Figure 4: Scatter plot — mean latency vs throughput per platform/precision.
    Bubble size = model size (MB). Red dashed line = 5G MEC budget (10ms).
    """
    fig, axes = plt.subplots(1, 2, figsize=(7.0, 3.4))

    # Left panel: Latency comparison (bar chart)
    ax1 = axes[0]
    platform_names = list(latency.keys())
    short_names = [
        n.replace('Intel Xeon-D','Xeon-D').replace('Jetson AGX Orin','Jetson')
         .replace('ARM Cortex-A72','ARM A72').replace('Teacher FP32 (Xeon-D)','Teacher\n(Xeon-D)')
        for n in platform_names
    ]
    mean_ms = [latency[p]['mean_ms'] for p in platform_names]
    p99_ms  = [latency[p]['p99_ms']  for p in platform_names]
    bar_colors = [
        COLORS['baseline_ml'] if 'Teacher' in p else
        COLORS['edgephish_int8'] if 'INT8' in p else
        COLORS['edgephish_fp32']
        for p in platform_names
    ]

    x = np.arange(len(platform_names))
    b1 = ax1.bar(x - 0.2, mean_ms, 0.35, label='Mean',   color=bar_colors, alpha=0.85)
    b2 = ax1.bar(x + 0.2, p99_ms,  0.35, label='P99',    color=bar_colors, alpha=0.45,
                 edgecolor=bar_colors, linewidth=1.2)

    # 5G budget line
    ax1.axhline(10.0, color=COLORS['highlight'], linestyle='--', linewidth=1.5,
                label='5G MEC Budget (10ms)', zorder=5)
    ax1.axhline(1.0, color=COLORS['alpha_line'], linestyle=':', linewidth=1.2,
                label='GPU Target (1ms)', zorder=5)

    ax1.set_xticks(x)
    ax1.set_xticklabels(short_names, fontsize=6.8, rotation=15, ha='right')
    ax1.set_ylabel('Latency (ms)', fontsize=9)
    ax1.set_title('(a) Inference Latency per Platform', fontsize=9, pad=5)
    ax1.legend(fontsize=7, loc='upper left')
    ax1.set_ylim(0, max(p99_ms) * 1.15)

    # Right panel: Throughput vs Size bubble chart
    ax2 = axes[1]
    throughputs = [latency[p]['throughput_s'] for p in platform_names]
    sizes_mb    = [latency[p]['size_mb']      for p in platform_names]
    bubble_sizes = [max(s/3, 20) for s in sizes_mb]

    sc = ax2.scatter(sizes_mb, throughputs, s=bubble_sizes,
                     c=bar_colors, alpha=0.8, edgecolors='white', linewidths=1.2, zorder=4)

    for i, (name, tp, sz) in enumerate(zip(short_names, throughputs, sizes_mb)):
        offset_y = 800 if i % 2 == 0 else -1200
        ax2.annotate(name, (sz, tp), xytext=(sz+3, tp+offset_y),
                     fontsize=6.2, ha='left',
                     arrowprops=dict(arrowstyle='->', color='#9CA3AF', lw=0.7))

    ax2.set_xlabel('Model Size (MB)', fontsize=9)
    ax2.set_ylabel('Throughput (URLs/sec)', fontsize=9)
    ax2.set_title('(b) Throughput vs. Model Size', fontsize=9, pad=5)
    ax2.set_xscale('log')
    ax2.yaxis.set_major_formatter(plt.FuncFormatter(lambda x, _: f'{int(x):,}'))

    fig.suptitle('Figure 4: Edge Deployment Characterization across Hardware Platforms',
                 fontsize=9.5, y=1.01)
    plt.tight_layout()
    plt.savefig(save_path, dpi=300)
    plt.savefig(save_path.replace('.pdf', '.png'), dpi=300)
    plt.close()
    print(f'  ✓ Figure 4 saved: {save_path}')


